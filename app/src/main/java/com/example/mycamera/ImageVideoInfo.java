package com.example.mycamera;

public class ImageVideoInfo {

    // 显示的名称
    private String name;
    // 路径
    private String path;
    // 类型
    private String type;

    public ImageVideoInfo(String name, String path, String type) {
        this.name = name;
        this.path = path;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
